function chupetines(){
    let eleccion = window.confirm("Queres comprar los chupetines?")
     if (eleccion === true){
         let cantidad = window.prompt("Cuantos queres?")
         let precio = 30;
         let total = parseInt(cantidad) * precio;
         alert("Serian " + total + "$");
         alert("Gracias vuelvas prontos saluda")
     }
     else{
         alert("Gracias vuelvas prontos saluda")
     }
 }
